package com.jay.mvc.dto;

public class O3Dto {

    public O3Dto() {
        super();
    }

    private String secretKey;

    private String body;

    public String getSecretKey() {
        return secretKey;
    }

    public void setSecretKey(String secretKey) {
        this.secretKey = secretKey;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }
}
