package com.jay.mvc.domain;

import com.jw.domain.annotation.Table;

@Table(name = "dict_word")
public class Word {

    private long id;
    private int type = 0;
    private String k;
    private String v;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public String getK() {
        return k;
    }

    public void setK(String k) {
        this.k = k;
    }

    public String getV() {
        return v;
    }

    public void setV(String v) {
        this.v = v;
    }
}
